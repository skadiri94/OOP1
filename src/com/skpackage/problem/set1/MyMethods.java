package com.skpackage.problem.set1;

public class MyMethods {




    private static int cube(int a){

        return a * a * a;

        // return numCube;
    }

    public static boolean isEven(int a){

        return a % 2 == 0;
    }

    public static int xCube(int a){
        int x = cube(a);

        return x;
    }

}
